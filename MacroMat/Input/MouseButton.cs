namespace MacroMat.Input;

/// <summary>
/// Mouse button specifier in mouse simulation and events.
/// </summary>
public enum MouseButton
{
    /// <summary>
    /// The left mouse button.
    /// </summary>
    Left,
    /// <summary>
    /// The mouse wheel.
    /// </summary>
    VerticalWheel,
    /// <summary>
    /// The mouse wheel, shifted to horizontal mode (or for mice with horizontal wheels).
    /// </summary>
    HorizontalWheel,
    /// <summary>
    /// The right mouse button.
    /// </summary>
    Right
}